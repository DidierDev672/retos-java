package Class;

//! Proporcionalidad Inversa
//* Si 8 trabajadores tardan 6 dias en terminar un proyecto, cuantos dias tardara un solo trabajador en terminarlo?

public class Three {
    public static void main(String[] args) {
        double value1 = 8;
        double value2 = 6;
        double result1 = 1;

        double result2 = (value1 * value2) / result1;
        System.out.println("Tardara en dias: " + result2);
    }
}
